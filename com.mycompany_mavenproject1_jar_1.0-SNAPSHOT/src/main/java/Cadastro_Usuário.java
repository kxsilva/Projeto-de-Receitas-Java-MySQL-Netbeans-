    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        dadosUser atb = new dadosUser();
        user_DAO dao = new user_DAO();
        
        if(name.getText().isEmpty() || pass.getText().isEmpty() || email.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!");
            name.setText("");
            pass.setText("");
            email.setText("");
            
        }
        else if(!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", email.getText()))) { 
            JOptionPane.showMessageDialog(null, "Email não é válido!");
        }   
        else{
        atb.setName(name.getText());
        atb.setPass(pass.getText());
        atb.setEmail(email.getText());
        atb.setSex((String) sex.getSelectedItem());
        dao.create(atb);
         Tela_Inicial ti = new Tela_Inicial();
        ti.show();
        dispose();
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

   
